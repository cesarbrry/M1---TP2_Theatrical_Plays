package theatricalplays;

public class Customer {

    public String name;
    public int numeroClient;
    public int PointDeFidelite;

    public Customer(String name, int numeroClient, int PointDeFidelite)
    {
        this.name               = name;
        this.numeroClient       = numeroClient ;
        this.PointDeFidelite    = PointDeFidelite ;
    }

    public String getName(){return name;}

    public int getNumeroClient(){return numeroClient;}

    public int getPointDeFidelite(){return PointDeFidelite;}

}